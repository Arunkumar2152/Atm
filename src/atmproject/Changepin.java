        if(newpin.getText().isEmpty()|| cnewpin.getText().isEmpty())
        {
        JOptionPane.showMessageDialog(this, "Enter And Conform the Pin");       
        }else if(newpin.getText() != cnewpin.getText())
        {
           JOptionPane.showMessageDialog(this, "New Pin and Conform Pin was Not Equal"); 
        }
        else{
             try{
           String Query="UPDATE atmdb  set Pin=?  WHERE AccNum=?"; 
           //Class.forName("com.mysql.cj.jdbc.Driver");
           con=DriverManager.getConnection("jdbc:mysql://localhost/arun", "root", "arun2152");
            PreparedStatement ps=con.prepareStatement(Query);
            ps.setInt(1,Integer.valueOf(newpin.getText()));
            ps.setInt(2, MyAccount);
            if(ps.executeUpdate()==1){
                JOptionPane.showMessageDialog(this, "Pin Updated");
            }
            else{
                JOptionPane.showMessageDialog(this, "Missing the information");
 
            }
            }
            catch(Exception e){
                 JOptionPane.showMessageDialog(this, e);

            }
        }
